package NandhuDSA;

import java.util.Scanner;

public class Loops {
    public static void main(String[] args) {
        /*
        syntax of for loop :
        for (initialisation; condition; increment/decrement){
            // body
        }
         */

        // Question : Print numbers from 1 to 5
        for (int i=1; i<=5; i++){
            System.out.println(i);
        }
    }
}
